import { useState, useEffect, useRef, useCallback, useMemo } from 'react'
import Delaunator from 'delaunator'
import type { Project, Point2D, MeshData } from '../../types/project'
import type { UploadHint } from '../../db/projectsStore'
import TriangulationCanvas from '../triangulation/TriangulationCanvas'
import { useTriangulation } from '../triangulation/useTriangulation'
import { generateAutoMesh } from '../../utils/autoMeshGenerator'
import { generateTemplatePDF } from '../../utils/pdfGenerator'
import { computeAllBarycentrics, interpolateInternalPoint } from '../../utils/barycentricUtils'
import { pointInPolygon, triangleCentroid } from '../../utils/geometry'
import { computeAllSubdivisionFrames } from '../../utils/curvilinearContour'
import { loadOpenCVWorker } from '../../utils/perspectiveCorrection'
import type { PointType } from '../triangulation/drawingUtils'

interface Props {
  project: Project
  onSave: (project: Project, uploadOnly?: UploadHint[]) => Promise<void>
}

export default function TriangulationStep({ project, onSave }: Props) {
  const [saving, setSaving] = useState(false)
  const [imageUrl, setImageUrl] = useState<string | null>(null)
  const [density, setDensity] = useState(5)
  const [generating, setGenerating] = useState(false)
  const isAutoGenerated = useRef(false)

  const mesh = project.mesh
  const contourAnchors = mesh?.contourAnchors ?? []
  const contourSubPts = mesh?.contourSubdivisionPoints ?? []
  const contourSubParams = mesh?.contourSubdivisionParams ?? []
  const anchorPoints = mesh?.anchorPoints ?? []

  // Ordered contour polygon: anchor0 → sub_seg0 → anchor1 → sub_seg1 → ...
  // This is the proper winding-order polygon for pointInPolygon filtering
  const orderedContour = useMemo(() => {
    const n = contourAnchors.length
    if (n < 3) return contourAnchors
    const result: Point2D[] = []
    for (let i = 0; i < n; i++) {
      result.push(contourAnchors[i])
      // Collect subdivision points for this segment, sorted by t
      const segPts: { t: number; point: Point2D }[] = []
      for (let j = 0; j < contourSubParams.length; j++) {
        if (contourSubParams[j].segmentIndex === i) {
          segPts.push({ t: contourSubParams[j].t, point: contourSubPts[j] })
        }
      }
      segPts.sort((a, b) => a.t - b.t)
      for (const sp of segPts) {
        result.push(sp.point)
      }
    }
    return result
  }, [contourAnchors, contourSubPts, contourSubParams])

  // Tracked points = contour anchors + internal anchors (for barycentric interpolation)
  const trackedPoints = useMemo(() => [...contourAnchors, ...anchorPoints], [contourAnchors, anchorPoints])
  // All base points for Delaunay (order must match videoFramesMesh convention)
  const allBasePoints = useMemo(() => [...contourAnchors, ...contourSubPts, ...anchorPoints], [contourAnchors, contourSubPts, anchorPoints])
  const topologyLocked = mesh?.topologyLocked ?? false
  const hasAnimation = mesh?.videoFramesMesh != null

  // Animation state
  const [computing, setComputing] = useState(false)
  const [animProgress, setAnimProgress] = useState('')
  const [playing, setPlaying] = useState(false)
  const [currentFrame, setCurrentFrame] = useState(0)
  const animCanvasRef = useRef<HTMLCanvasElement>(null)
  const animContainerRef = useRef<HTMLDivElement>(null)
  const videoRef = useRef<HTMLVideoElement | null>(null)
  const [videoReady, setVideoReady] = useState(false)
  const animFrameRef = useRef(0)
  const [imageDims, setImageDims] = useState<{ w: number; h: number } | null>(null)

  const {
    internalPoints,
    triangles,
    addInternalPoint,
    movePoint,
    deletePoint,
    loadAutoMesh,
  } = useTriangulation(
    mesh
      ? {
          contourPoints: orderedContour,
          internalPoints: mesh.internalPoints,
          triangles: mesh.triangles,
        }
      : undefined,
    allBasePoints.length > 0 ? allBasePoints : undefined,
    orderedContour.length >= 3 ? orderedContour : undefined
  )

  useEffect(() => {
    if (!project.originalImageBlob) {
      setImageUrl(null)
      return
    }
    const url = URL.createObjectURL(project.originalImageBlob)
    setImageUrl(url)
    return () => URL.revokeObjectURL(url)
  }, [project.originalImageBlob])

  const runAutoInternalPoints = useCallback(async (d: number) => {
    if (!project.originalImageBlob) return
    setGenerating(true)
    try {
      const result = await generateAutoMesh(project.originalImageBlob, d)
      loadAutoMesh(orderedContour, result.internalPoints)
      isAutoGenerated.current = true
    } catch (err) {
      console.error('Auto internal points failed:', err)
    } finally {
      setGenerating(false)
    }
  }, [project.originalImageBlob, loadAutoMesh, orderedContour])

  function handleDensityChange(delta: number) {
    const newDensity = Math.max(1, Math.min(10, density + delta))
    setDensity(newDensity)
    if (isAutoGenerated.current) {
      runAutoInternalPoints(newDensity)
    }
  }

  function handleClearInternalPoints() {
    loadAutoMesh(orderedContour, [])
    isAutoGenerated.current = false
  }

  const handleMovePoint = useCallback((type: PointType, index: number, p: Point2D) => {
    if (type === 'internal') {
      movePoint('internal', index, p)
    }
  }, [movePoint])

  const handleDeletePoint = useCallback((type: PointType, index: number) => {
    if (type === 'internal') {
      deletePoint('internal', index)
    }
  }, [deletePoint])

  async function handleLockTopology() {
    if (!mesh || trackedPoints.length < 3) return
    setSaving(true)
    try {
      // Delaunay on tracked points (contour + anchors)
      const coords = new Float64Array(trackedPoints.length * 2)
      trackedPoints.forEach((p, i) => {
        coords[i * 2] = p.x
        coords[i * 2 + 1] = p.y
      })
      const delaunay = new Delaunator(coords)
      const trackedTriangles: [number, number, number][] = []
      for (let i = 0; i < delaunay.triangles.length; i += 3) {
        const a = delaunay.triangles[i]
        const b = delaunay.triangles[i + 1]
        const c = delaunay.triangles[i + 2]
        const centroid = triangleCentroid(trackedPoints[a], trackedPoints[b], trackedPoints[c])
        if (pointInPolygon(centroid, orderedContour)) {
          trackedTriangles.push([a, b, c])
        }
      }

      // Compute barycentrics for internal points relative to tracked points
      const internalBarycentrics = computeAllBarycentrics(
        internalPoints,
        trackedPoints,
        trackedTriangles
      )

      const updatedMesh: MeshData = {
        ...mesh,
        internalPoints,
        triangles,
        topologyLocked: true,
        trackedTriangles,
        internalBarycentrics,
      }

      await onSave({ ...project, mesh: updatedMesh })
    } catch (err) {
      console.error('Failed to lock topology:', err)
      alert('Erreur : ' + (err instanceof Error ? err.message : err))
    }
    setSaving(false)
  }

  async function handleUnlockTopology() {
    if (!mesh) return
    if (!confirm('Déverrouiller la topologie supprimera les données d\'animation. Continuer ?')) return
    setSaving(true)
    try {
      const updatedMesh: MeshData = {
        ...mesh,
        topologyLocked: false,
        trackedTriangles: [],
        internalBarycentrics: [],
        videoFramesMesh: null,
      }
      await onSave({ ...project, mesh: updatedMesh })
    } catch (err) {
      console.error('Failed to unlock topology:', err)
    }
    setSaving(false)
  }

  async function handleSave() {
    if (!mesh) return
    setSaving(true)
    try {
      const updatedMesh: MeshData = {
        ...mesh,
        internalPoints,
        triangles,
      }
      await onSave({ ...project, mesh: updatedMesh })
    } catch (err) {
      console.error('Failed to save mesh:', err)
      alert('Erreur lors de la sauvegarde : ' + (err instanceof Error ? err.message : err))
    }
    setSaving(false)
  }

  // --- Animation computation ---

  // Load image dimensions
  useEffect(() => {
    if (!project.originalImageBlob) return
    const url = URL.createObjectURL(project.originalImageBlob)
    const img = new Image()
    img.onload = () => {
      setImageDims({ w: img.naturalWidth, h: img.naturalHeight })
      URL.revokeObjectURL(url)
    }
    img.onerror = () => URL.revokeObjectURL(url)
    img.src = url
  }, [project.originalImageBlob])

  // Load video for preview
  useEffect(() => {
    if (!project.videoBlob) return
    const url = URL.createObjectURL(project.videoBlob)
    const video = document.createElement('video')
    video.src = url
    video.muted = true
    video.playsInline = true
    video.preload = 'auto'
    video.onloadeddata = () => {
      videoRef.current = video
      setVideoReady(true)
    }
    video.load()
    return () => { video.pause(); URL.revokeObjectURL(url) }
  }, [project.videoBlob])

  // Canvas resize
  useEffect(() => {
    const container = animContainerRef.current
    const canvas = animCanvasRef.current
    if (!container || !canvas) return
    const observer = new ResizeObserver(entries => {
      const { width, height } = entries[0].contentRect
      if (width === 0 || height === 0) return
      const dpr = window.devicePixelRatio || 1
      canvas.width = width * dpr
      canvas.height = height * dpr
      canvas.style.width = `${width}px`
      canvas.style.height = `${height}px`
    })
    observer.observe(container)
    return () => observer.disconnect()
  }, [hasAnimation])

  // Animation playback loop
  useEffect(() => {
    if (!hasAnimation || !playing) return
    const frames = mesh!.videoFramesMesh!
    let lastTime = performance.now()
    function tick(now: number) {
      if (now - lastTime >= 1000 / 24) {
        lastTime = now
        setCurrentFrame(f => (f + 1) % frames.length)
      }
      animFrameRef.current = requestAnimationFrame(tick)
    }
    animFrameRef.current = requestAnimationFrame(tick)
    return () => cancelAnimationFrame(animFrameRef.current)
  }, [hasAnimation, playing, mesh])

  // Draw animation frame
  useEffect(() => {
    if (!hasAnimation || !videoReady) return
    const canvas = animCanvasRef.current
    const video = videoRef.current
    const frames = mesh!.videoFramesMesh!
    const tris = mesh!.triangles
    if (!canvas || !video || currentFrame >= frames.length) return

    // Ensure canvas has dimensions (ResizeObserver may not have fired yet)
    if (canvas.width === 0 || canvas.height === 0) {
      const rect = canvas.getBoundingClientRect()
      if (rect.width > 0 && rect.height > 0) {
        const dpr = window.devicePixelRatio || 1
        canvas.width = rect.width * dpr
        canvas.height = rect.height * dpr
      }
    }

    const frame = currentFrame // capture for closure
    const targetTime = frame / 24

    function draw() {
      const ctx = canvas!.getContext('2d')
      if (!ctx) return
      const dpr = window.devicePixelRatio || 1
      ctx.setTransform(dpr, 0, 0, dpr, 0, 0)
      const cssW = canvas!.width / dpr
      const cssH = canvas!.height / dpr
      if (cssW === 0 || cssH === 0) return
      ctx.clearRect(0, 0, cssW, cssH)
      const vw = video!.videoWidth, vh = video!.videoHeight
      const s = Math.min(cssW / vw, cssH / vh) * 0.95
      const ox = (cssW - vw * s) / 2, oy = (cssH - vh * s) / 2
      ctx.drawImage(video!, ox, oy, vw * s, vh * s)
      const imgW = imageDims?.w ?? vw, imgH = imageDims?.h ?? vh
      const points = frames[frame]
      ctx.strokeStyle = 'rgba(0, 255, 100, 0.4)'
      ctx.lineWidth = 1
      for (const [a, b, c] of tris) {
        if (a >= points.length || b >= points.length || c >= points.length) continue
        const pa = points[a], pb = points[b], pc = points[c]
        ctx.beginPath()
        ctx.moveTo((pa.x / imgW) * vw * s + ox, (pa.y / imgH) * vh * s + oy)
        ctx.lineTo((pb.x / imgW) * vw * s + ox, (pb.y / imgH) * vh * s + oy)
        ctx.lineTo((pc.x / imgW) * vw * s + ox, (pc.y / imgH) * vh * s + oy)
        ctx.closePath()
        ctx.stroke()
      }
      ctx.fillStyle = 'rgba(0,0,0,0.6)'
      ctx.fillRect(8, 8, 120, 24)
      ctx.fillStyle = '#fff'
      ctx.font = '12px monospace'
      ctx.fillText(`Frame ${frame} / ${frames.length - 1}`, 14, 24)
    }

    const handleSeeked = () => draw()
    video.addEventListener('seeked', handleSeeked)
    video.currentTime = targetTime
    // Draw immediately if already at target time
    if (Math.abs(video.currentTime - targetTime) < 0.02) {
      draw()
    }
    return () => {
      video.removeEventListener('seeked', handleSeeked)
    }
  }, [hasAnimation, videoReady, currentFrame, mesh, imageDims])

  async function handleComputeAnimation() {
    if (!mesh || !mesh.contourAnchorFrames || !mesh.anchorFrames) return
    const contourAnchorFrames = mesh.contourAnchorFrames
    const ancFrames = mesh.anchorFrames
    const trackedTris = mesh.trackedTriangles
    const intBary = mesh.internalBarycentrics

    const totalF = Math.min(contourAnchorFrames.length, ancFrames.length)
    if (totalF === 0) return

    setComputing(true)

    try {
      // Compute subdivision frames if not already done
      let contourSubFrames = mesh.contourSubdivisionFrames
      if (!contourSubFrames && mesh.contourSubdivisionParams.length > 0 && project.videoBlob && mesh.cannyParams) {
        setAnimProgress('Calcul positions contour subdivision...')
        await loadOpenCVWorker()
        contourSubFrames = await computeAllSubdivisionFrames(
          project.videoBlob,
          contourAnchorFrames,
          mesh.contourSubdivisionParams,
          mesh.cannyParams,
          (p) => setAnimProgress(`Subdivision frame ${p.frame}/${p.total}`)
        )
      }

      setAnimProgress('Calcul animation...')
      const videoFramesMesh: Point2D[][] = []

      for (let f = 0; f < totalF; f++) {
        if (f % 10 === 0) {
          setAnimProgress(`Frame ${f + 1} / ${totalF}`)
          await new Promise(r => setTimeout(r, 0))
        }

        // Tracked positions for barycentric interpolation
        const trackedPositions = [...contourAnchorFrames[f], ...ancFrames[f]]

        // Subdivision positions (from curvilinear computation)
        const subPositions = contourSubFrames?.[f] ?? mesh.contourSubdivisionPoints

        // Internal points via barycentrics
        const internalPositions = intBary.map(bary =>
          interpolateInternalPoint(bary, trackedPositions, trackedTris)
        )

        // allPoints = [...contourAnchors, ...contourSubdivisionPoints, ...anchorPoints, ...internalPoints]
        videoFramesMesh.push([
          ...contourAnchorFrames[f],
          ...subPositions,
          ...ancFrames[f],
          ...internalPositions,
        ])
      }

      setAnimProgress('Sauvegarde...')
      const updatedMesh: MeshData = {
        ...mesh,
        contourSubdivisionFrames: contourSubFrames,
        contourSubdivisionValidated: true,
        videoFramesMesh,
      }
      await onSave({ ...project, mesh: updatedMesh }, ['contourSubdivisionFrames', 'videoFramesMesh'])
      setComputing(false)
      setAnimProgress('')
    } catch (err) {
      console.error('Animation computation failed:', err)
      alert('Erreur : ' + (err instanceof Error ? err.message : err))
      setComputing(false)
      setAnimProgress('')
    }
  }

  async function handleGeneratePDF() {
    try {
      const blob = await generateTemplatePDF(project)
      const url = URL.createObjectURL(blob)
      const a = document.createElement('a')
      a.href = url
      a.download = `${project.name}-template.pdf`
      a.click()
      URL.revokeObjectURL(url)
    } catch (err) {
      console.error('PDF generation failed:', err)
      alert('Erreur PDF : ' + (err instanceof Error ? err.message : err))
    }
  }

  if (!mesh?.anchorTrackingValidated) {
    return (
      <div className="placeholder">
        Validez d&apos;abord le tracking des ancres (étape 7).
      </div>
    )
  }

  if (trackedPoints.length < 3) {
    return (
      <div className="placeholder">
        Il faut au minimum 3 points trackés (contour + ancres) pour trianguler.
      </div>
    )
  }

  return (
    <div className="triangulation-step">
      <div className="triangulation-toolbar">
        {topologyLocked && (
          <span style={{ color: '#22c55e', fontWeight: 'bold', marginRight: 8 }}>
            Topologie verrouillée
          </span>
        )}

        {!topologyLocked && (
          <>
            <button onClick={() => runAutoInternalPoints(density)} disabled={generating}>
              {generating ? 'Génération...' : 'Auto points internes'}
            </button>

            <button
              onClick={() => handleDensityChange(-1)}
              disabled={density <= 1 || generating}
              title="Moins de points"
            >
              −
            </button>
            <span className="density-label">Densité: {density}</span>
            <button
              onClick={() => handleDensityChange(1)}
              disabled={density >= 10 || generating}
              title="Plus de points"
            >
              +
            </button>

            <span className="toolbar-separator" />

            <button onClick={handleSave} disabled={saving}>
              {saving ? 'Sauvegarde...' : 'Sauvegarder'}
            </button>
            <button
              onClick={handleLockTopology}
              disabled={saving || triangles.length === 0}
              style={{ background: '#22c55e', color: 'white' }}
            >
              Verrouiller la topologie
            </button>
            <button className="btn-danger" onClick={handleClearInternalPoints}>
              Effacer internes
            </button>
          </>
        )}

        {topologyLocked && (
          <button className="btn-danger" onClick={handleUnlockTopology} disabled={saving}>
            Déverrouiller
          </button>
        )}

        <span className="toolbar-separator" />

        <button onClick={handleGeneratePDF}>
          Télécharger PDF
        </button>

        <span className="toolbar-info">
          Contour: {contourAnchors.length}+{contourSubPts.length} |
          Ancres: {anchorPoints.length} |
          Internes: {internalPoints.length} |
          Triangles: {triangles.length}
        </span>
      </div>

      <div className="triangulation-help">
        {topologyLocked ? (
          <span>
            La topologie est verrouillée. Calculez l&apos;animation ci-dessous.
          </span>
        ) : (
          <span>
            Clic gauche = ajouter un point interne | Glisser = déplacer | Clic droit = supprimer |
            Contour et ancres (bleus + orange) sont en lecture seule.
          </span>
        )}
      </div>

      <TriangulationCanvas
        imageUrl={imageUrl}
        contourPoints={orderedContour}
        internalPoints={topologyLocked ? mesh.internalPoints : internalPoints}
        triangles={topologyLocked ? mesh.triangles : triangles}
        contourClosed={true}
        mode="internal"
        onAddContourPoint={() => {}}
        onInsertContourPoint={() => {}}
        onCloseContour={() => {}}
        onAddInternalPoint={topologyLocked ? () => {} : addInternalPoint}
        onMovePoint={topologyLocked ? () => {} : handleMovePoint}
        onDeletePoint={topologyLocked ? () => {} : handleDeletePoint}
        anchorPoints={allBasePoints}
        readOnlyAnchors={true}
      />

      {/* Animation section (when topology locked) */}
      {topologyLocked && (
        <div style={{ borderTop: '2px solid #333', marginTop: 8 }}>
          <div style={{ padding: '12px 16px', display: 'flex', alignItems: 'center', gap: 12 }}>
            {!hasAnimation ? (
              <>
                <button
                  onClick={handleComputeAnimation}
                  disabled={computing}
                  style={{ background: '#2563eb', color: 'white', padding: '8px 24px' }}
                >
                  {computing ? 'Calcul en cours...' : 'Calculer l\'animation'}
                </button>
                {computing && <span style={{ fontFamily: 'monospace', color: '#888' }}>{animProgress}</span>}
              </>
            ) : (
              <>
                <span style={{ color: '#22c55e', fontWeight: 'bold' }}>Animation calculée</span>
                <span style={{ color: '#888' }}>
                  {mesh.videoFramesMesh!.length} frames, {mesh.videoFramesMesh![0]?.length ?? 0} pts/frame
                </span>
                <button onClick={() => setPlaying(!playing)}>{playing ? 'Pause' : 'Play'}</button>
                <button onClick={() => { setPlaying(false); setCurrentFrame(0) }}>Rewind</button>
                <button onClick={handleComputeAnimation} disabled={computing} style={{ marginLeft: 'auto' }}>
                  Recalculer
                </button>
              </>
            )}
          </div>

          {computing && (
            <div style={{ padding: '0 16px' }}>
              <div style={{ width: '100%', height: 4, background: '#333', borderRadius: 2 }}>
                <div style={{ width: '50%', height: '100%', background: '#2563eb', borderRadius: 2 }} />
              </div>
            </div>
          )}

          {hasAnimation && (
            <div ref={animContainerRef} style={{ height: 400, position: 'relative' }}>
              <canvas ref={animCanvasRef} style={{ width: '100%', height: '100%' }} />
            </div>
          )}
        </div>
      )}
    </div>
  )
}
